package com.monkeygang.mindfactorybooking.Objects;

public class Catering {

    int id;

    String name;

    public Catering(int id, String name) {
        this.id = id;
        this.name = name;
    }


    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

}
